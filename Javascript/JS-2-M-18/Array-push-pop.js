// push means add element or data to an array
var friendsAge = [11, 13, 17, 12]
friendsAge.push(15);
console.log(friendsAge)


// Pop means remove element or data from an array
friendsAge.pop()
friendsAge.pop()
console.log(friendsAge)


// shift() removes the first element
var removeInFirstElement = ["Aam", "Jam", "Kathal", "Lichu", "Komola", "Anarosh"]
console.log(removeInFirstElement)
removeInFirstElement.shift()
removeInFirstElement.shift()
console.log(removeInFirstElement);


// unshift() adds an element to the beginning of the array
var addInFirstElement = ["Baba", "Maa", "Vai", "Bon"]
console.log(addInFirstElement)

addInFirstElement.unshift("Nani")
addInFirstElement.unshift("Nana")
console.log(addInFirstElement)